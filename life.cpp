using namespace std;

void initialize(vector<vector<char> >& ecosystem) {
    int numCols = ecosystem[0].size();
    int numRows = ecosystem.size();
    int rowPopStart = numRows/2 - numRows/5/2;
    int colPopStart = numCols/2 - numCols/5/2;
    int rowPopEnd = numRows/2 + numRows/5/2;
    int colPopEnd = numCols/2 + numCols/5/2;
    srand(time(0));

    for (int i = rowPopStart; i < rowPopEnd; i++) {
        for (int j = colPopStart; j < colPopEnd; j++) {
            if (rand() % 2)
            ecosystem[i][j] = '*';
        }
    }
}

void visualize(vector<vector<char> >& ecosystem) {
    for (int i = 0; i < ecosystem.size(); i++) {
        for (int j = 0; j < ecosystem[i].size(); j++) {
            cout << ecosystem[i][j];
        }
        cout << "\n";
    }
    cout << "\n---------------------------------------------------------------------------------\n";
    this_thread::sleep_for(chrono::milliseconds(500));
}

bool isValidCell(int row, int col, int numRows, int numCols) {
    return (row >= 0 && row < numRows && col >= 0 && col < numCols);
}

void life(vector<vector<char> >& ecosystem){
    int numRows = ecosystem.size();
    int numCols = ecosystem[0].size();

    initialize(ecosystem);

    while (true) {
        
        visualize(ecosystem);

        for (int i = 0; i < numRows; i++) {
            for (int j = 0; j < numCols; j++) {
                int numNeighbors = 0;

                int rowIndex[] = {-1, -1, -1, 0, 0, 1, 1, 1};
                int colIndex[] = {-1, 0, 1, -1, 1, -1, 0, 1};

                for (int x = 0; x < 8; ++x) {
                    int neighborRow = i + rowIndex[x];
                    int neighborCol = j + colIndex[x];

                    if (isValidCell(neighborRow, neighborCol, numRows, numCols)) {
                        if (ecosystem[neighborRow][neighborCol] == '*')
                            numNeighbors += 1;
                    }
                }

                if (ecosystem[i][j] == '*') {
                    if (numNeighbors < 2 || numNeighbors > 3)
                        ecosystem[i][j] = ' ';
                } else {
                    if (numNeighbors == 3)
                        ecosystem[i][j] = '*';
                }
            }
        }
    }
}

